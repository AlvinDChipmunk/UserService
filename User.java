package com.hubgitalvin.wk3.usrassign;

public class User {

	private String userName; 
	private String passWord; 
	private String name; 
	
	public void setUserName(String uUName) { this.userName = uUName; }
	public void setPassword(String uPWord) { this.passWord = uPWord; }
	public void setName(String uName) { this.name = uName; }

	public String getUserName() { return userName; }
	public String getPassword() { return passWord; }
	public String getName() { return name; }

}
